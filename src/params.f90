!======================================================================!
module params
!----------------------------------------------------------------------!
implicit none
!----------------------------------------------------------------------!
! Number of years in simulation (yr)
!----------------------------------------------------------------------!
integer, parameter :: nyr = 40
!----------------------------------------------------------------------!
! Number of model timsteps in year                                   (n)
!----------------------------------------------------------------------!
integer, parameter :: ntimes = 1460
!----------------------------------------------------------------------!
! Seconds in a day                                             (s day-1)
!----------------------------------------------------------------------!
integer, parameter :: sday = 24 * 60 * 60
!----------------------------------------------------------------------!
! Seconds in a year                                             (s yr-1)
!----------------------------------------------------------------------!
integer, parameter :: syr = 365 * sday
!----------------------------------------------------------------------!
real, parameter :: zero = 0.0
real, parameter :: one = 1.0
!----------------------------------------------------------------------!
! Limit to stop dbz                                                  (-)
!----------------------------------------------------------------------!
real, parameter :: eps = 1.0e-8
!----------------------------------------------------------------------!
real, parameter :: pi = 3.14159
!----------------------------------------------------------------------!
! Molecular weight of carbon                                (g[C] mol-1)
!----------------------------------------------------------------------!
real, parameter :: MC     = 12.011
!----------------------------------------------------------------------!
! Main model timestep                                                (s)
!----------------------------------------------------------------------!
real, parameter :: dt = syr / float (ntimes)
!----------------------------------------------------------------------!
! C content of woody DM.  From Martin and Thomas, 2011.   (g[C] g[DM]-1)
!----------------------------------------------------------------------!
real, parameter :: CDM   = 0.474
!----------------------------------------------------------------------!
! C content of foliage DM. From ?                         (g[C] g[DM]-1)
!----------------------------------------------------------------------!
real, parameter :: gamma_fol = 0.481
!----------------------------------------------------------------------!
! C content of sucrose                               (g[C] g[Sucrose]-1)
!----------------------------------------------------------------------!
real, parameter :: fCSucrose = 144.1 / 342.3
!----------------------------------------------------------------------!
! C content of starch, C6H10O5                        (g[C] g[Starch]-1)
!----------------------------------------------------------------------!
real, parameter :: fCStarch  = 72.1 / 162.1
!----------------------------------------------------------------------!
! Wood density. From Nogueira et al., 2005.                  (kg[DM]/m3)
!----------------------------------------------------------------------!
real, parameter :: rho_wood = 675.0
!----------------------------------------------------------------------!
! Initial ratio of height to rb                                    (m/m)
!----------------------------------------------------------------------!
real, parameter :: hrat = 150.0
!----------------------------------------------------------------------!
! Initial ratio of rooting depth to height                         (m/m)
!----------------------------------------------------------------------!
real, parameter :: drat = 62.4 / 243.3
!----------------------------------------------------------------------!
! Ratio of radius at top of stem to base of stem                   (m/m)
!----------------------------------------------------------------------!
real, parameter :: alpha_ag = 0.54
!----------------------------------------------------------------------!
! Ratio of radius at bottom of root to base of stem                (m/m)
!----------------------------------------------------------------------!
real, parameter :: alpha_bg = 1.62
!----------------------------------------------------------------------!
! Parameters to compute mass from radius and height                  (-)
!----------------------------------------------------------------------!
real, parameter :: Chia = one + alpha_ag + alpha_ag ** 2
real, parameter :: Chib = one + alpha_bg + alpha_bg ** 2
!----------------------------------------------------------------------!
! Coefficient a_p for CPA from rb. From Pretzsch 2015 (Table 3).   ()
!----------------------------------------------------------------------!
real, parameter :: a_p = -0.8683
!----------------------------------------------------------------------!
! Coefficient alpha_p for CPA from rb (-)
!----------------------------------------------------------------------!
real, parameter :: alpha_p = 1.3149
!----------------------------------------------------------------------!
! Leaf area/sapwood area ratio                                   (m2/m2)
! From Friend et al. (1997), Table 3 for broadleaf.
!----------------------------------------------------------------------!
real, parameter :: lasa = 4167.0
!----------------------------------------------------------------------!
! Fine root/sapwood area ratio (m2/m2)
! Assumed == lasa
!----------------------------------------------------------------------!
real, parameter :: frasa = lasa
!----------------------------------------------------------------------!
! Specific leaf area                                         (m2/kg[DM])
! From Friend et al. (1997), Table 3 for broadleaf.
!----------------------------------------------------------------------!
real, parameter :: sla = gamma_fol * 36.0
!----------------------------------------------------------------------!
! Specific fine root area                                        (m2/m2)
! Assumed == sla
!----------------------------------------------------------------------!
real, parameter :: sfra = sla
!----------------------------------------------------------------------!
! Neutral sucrose concentration                          (g[Su] g[DM]-1)
! From Wueth et al. (2005).
!----------------------------------------------------------------------!
real, parameter :: KSu = 0.0288
!----------------------------------------------------------------------!
! Hill coefficient (-)
!----------------------------------------------------------------------!
integer, parameter :: n_Hill = 4
!----------------------------------------------------------------------!
! Maximum starch concentration                           (g[Su] g[DM]-1)
! From Herrera-Ramirez et al. (2021; https://doi.org/10.1111/nph.17239),
! a relatively high value among those sampled, tropical trees.
!----------------------------------------------------------------------!
real, parameter :: KSt = 0.1
!----------------------------------------------------------------------!
! Decay rate constant of An_base                                     (s)
! Just a guess of two weeks.
!----------------------------------------------------------------------!
real :: tau_Anbase = 14.0 * float (sday)
!----------------------------------------------------------------------!
! Decay rate constant of PLC                                         (s)
! Just a guess of two days, or was it from somewhere?
!----------------------------------------------------------------------!
real :: tau_PLC = 2.0 * float (sday)
!----------------------------------------------------------------------!
! Baseline rb growth rate                                        (m s-1)
! Default rate under ideal conditions will be 50% of this due to the
! sucrose effect.
!----------------------------------------------------------------------!
real, parameter :: drb_base  = 0.01 / float (syr)
!----------------------------------------------------------------------!
! Baseline height growth rate                                    (m s-1)
! Default rate under ideal conditions will be 50% of this due to the
! sucrose effect. Where from?
!----------------------------------------------------------------------!
real, parameter :: dht_base = (2.0 * 43.0 / 240.0) / float (syr)
!----------------------------------------------------------------------!
! Baseline rooting depth growth rate                             (m s-1)
! Default rate under ideal conditions will be 50% of this due to the
! sucrose effect. Complete guess
!----------------------------------------------------------------------!
real, parameter :: ddp_base = dht_base / 10.0
!----------------------------------------------------------------------!
! Maximum foliage growth rate calibrated to grow full crown in about
! 10 days                                      (kg[DM] m[sapwood]-2 s-1)
!----------------------------------------------------------------------!
real, parameter :: dMfol_base = 4.0e-4
!----------------------------------------------------------------------!
! Just assume same for fine roots.
!----------------------------------------------------------------------!
real, parameter :: dMfro_base = dMfol_base
!----------------------------------------------------------------------!
! Foliage residence time                                             (s)
! Where from?
!----------------------------------------------------------------------!
real, parameter :: tau_fol = 1.67 * float (syr)
!----------------------------------------------------------------------!
! Fine root residence time                                           (s)
! Where from?
!----------------------------------------------------------------------!
real, parameter :: tau_fro = 1.56 * float (syr)
!----------------------------------------------------------------------!
! Woody residence time (s)
! Where from?
!----------------------------------------------------------------------!
real, parameter :: tau_w = 180.0 * float (syr)
!----------------------------------------------------------------------!
! Growth yield. From Cannell and Thornley (2000)           (g[C] g[C]-1)
!----------------------------------------------------------------------!
real, parameter :: YG = 0.8
!----------------------------------------------------------------------!
! Maintenance respiration constant at 20oC           (kg[C] kg[N]-1 s-1)
! From Cannell and Thornley (2000) for EFM.
!----------------------------------------------------------------------!
real, parameter :: kmmax = 0.1 / float (sday)
!----------------------------------------------------------------------!
! Michaelis-Menten constant for residual respiration        (g[C] g[DM])
! From Cannell and Thornley (2000) for EFM.
!----------------------------------------------------------------------!
real, parameter :: Km_R = 0.05
!----------------------------------------------------------------------!
! Fraction of compartment biomass that is N                   (fraction)
! Taken from Collelongo, Scarascia et al. (2000).
!----------------------------------------------------------------------!
real, parameter :: fNfol = 24.22 / 1000.0
real, parameter :: fNstm = (1.29+0.67) / 2000.0 ! mean branches+stem
real, parameter :: fNcro = 0.04 / 1000.0
real, parameter :: fNfro = 4.42 / 1000.0
!----------------------------------------------------------------------!
! Q10 for respiration                                           (scalar)
! From Crous et al. (2022) this should not be a constant.
!----------------------------------------------------------------------!
real, parameter :: Q10_R = 2.0
!----------------------------------------------------------------------!
! Respiration associated with phloem loading constant      (g[C] d[C]-1)
! Taken from Cannell and Thornley (2000).
!----------------------------------------------------------------------!
real, parameter :: cphloem = 0.06
!----------------------------------------------------------------------!
! Coefficient for starch synthesis                 (kg[St] kg[DM]-1 s-1)
! Where from?
!----------------------------------------------------------------------!
real, parameter :: alpha_St = 2.3e-8
!----------------------------------------------------------------------!
end module params
!======================================================================!
